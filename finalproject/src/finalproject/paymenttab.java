    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String searchSql = "SELECT * FROM paymenttbl WHERE ID = ?";
int searchId;

// Input validation for Search ID
String searchIdString = ids.getText().trim();
System.out.println("Search ID Input: " + searchIdString + "'");

if (searchIdString.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Please enter a Member ID to search.");
    return;
}

try {
    searchId = Integer.parseInt(searchIdString);
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Invalid Member ID. Please enter a valid integer.");
    return;
}

// Search for data in the database
try (PreparedStatement searchPst = con.prepareStatement(searchSql)) {
    searchPst.setInt(1, searchId);

    try (ResultSet resultSet = searchPst.executeQuery()) {
        if (resultSet.next()) {
            // Retrieve data from the ResultSet
            String foundName = resultSet.getString("Name");
            String foundMonth = resultSet.getString("Month");
            double foundAmount = resultSet.getDouble("Amount");

            // Display the retrieved data in text fields
            SwingUtilities.invokeLater(() -> {
                name.setText(foundName);
                months.setText(foundMonth);
                amounts.setText(String.valueOf(foundAmount));
            });
        } else {
            JOptionPane.showMessageDialog(null, "Record not found for Member ID: " + searchId);
        }
    }
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(null, ex);
}
    }//GEN-LAST:event_jButton1ActionPerformed

